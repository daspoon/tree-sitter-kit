module.exports = grammar({
    name: 'TypedLang',
    rules: {
        Block: $ => seq(field('0', optional($.DefSequenceDelimitedBySemicolon)), field('1', $.Expr)),
        Def: $ => choice($.Def_enum, $.Def_fun, $.Def_let, $.Def_typedef),
        DefSequenceDelimitedBySemicolon: $ => seq($.Def, ';', repeat(seq($.Def, ';'))),
        Def_enum: $ => field('0', $.Enum),
        Def_fun: $ => field('0', $.Fun),
        Def_let: $ => field('0', $.Let),
        Def_typedef: $ => field('0', $.Typedef),
        Enum: $ => seq('enum', field('0', $.Name), '{', field('1', $.EnumCaseSequenceSeparatedByComma), '}'),
        EnumCase: $ => seq(field('0', $.Name), field('1', optional($.ParamArray))),
        EnumCaseSequenceSeparatedByComma: $ => seq($.EnumCase, repeat(seq(',', $.EnumCase))),
        Expr: $ => choice($.Expr_add, $.Expr_and, $.Expr_block, $.Expr_call, $.Expr_eql, $.Expr_lambda, $.Expr_match, $.Expr_mu, $.Expr_mul, $.Expr_name, $.Expr_neg, $.Expr_numb, $.Expr_or, $.Expr_paren, $.Expr_pow, $.Expr_project),
        ExprArray: $ => seq('(', optional(seq($.Expr, repeat(seq(',', $.Expr)))), ')'),
        Expr_add: $ => prec.left(4, seq(field('0', $.Expr), field('1', choice('+', '-')), field('2', $.Expr))),
        Expr_and: $ => prec.left(3, seq(field('0', $.Expr), field('1', choice('&&')), field('2', $.Expr))),
        Expr_block: $ => seq('{', field('0', $.Block), '}'),
        Expr_call: $ => prec.left(9, seq(field('0', $.Expr), field('1', $.ExprArray))),
        Expr_eql: $ => prec.left(1, seq(field('0', $.Expr), field('1', choice('==')), field('2', $.Expr))),
        Expr_lambda: $ => seq('!', field('0', $.ParamArray), '->', field('1', $.Type), '.', field('2', $.Expr)),
        Expr_match: $ => seq('match', field('0', $.Expr), '{', field('1', $.MatchCaseSequenceSeparatedByComma), '}'),
        Expr_mu: $ => seq('!', field('0', $.Name), field('1', $.ParamArray), '->', field('2', $.Type), '.', field('3', $.Expr)),
        Expr_mul: $ => prec.left(5, seq(field('0', $.Expr), field('1', choice('*', '/', '%')), field('2', $.Expr))),
        Expr_name: $ => field('0', $.Name),
        Expr_neg: $ => prec(7, seq(field('0', choice('-')), field('1', $.Expr))),
        Expr_numb: $ => field('0', $.Int),
        Expr_or: $ => prec.left(2, seq(field('0', $.Expr), field('1', choice('||')), field('2', $.Expr))),
        Expr_paren: $ => field('0', $.ExprArray),
        Expr_pow: $ => prec.right(6, seq(field('0', $.Expr), field('1', choice('^')), field('2', $.Expr))),
        Expr_project: $ => prec.left(8, seq(field('0', $.Expr), '.', field('1', $.Int))),
        Fun: $ => seq('fun', field('0', $.Name), field('1', $.ParamArray), '->', field('2', $.Type), '{', field('3', $.Expr), '}'),
        Int: $ => /[0-9]+/,
        Let: $ => seq('let', field('0', $.Param), '=', field('1', $.Expr)),
        MatchCase: $ => seq(field('0', $.Name), field('1', $.NameArray), '=>', field('2', $.Expr)),
        MatchCaseSequenceSeparatedByComma: $ => seq($.MatchCase, repeat(seq(',', $.MatchCase))),
        Name: $ => /[a-zA-Z_]+[0-9a-zA-Z_]*/,
        NameArray: $ => seq('(', optional(seq($.Name, repeat(seq(',', $.Name)))), ')'),
        Param: $ => seq(field('0', $.Name), ':', field('1', $.Type)),
        ParamArray: $ => seq('(', optional(seq($.Param, repeat(seq(',', $.Param)))), ')'),
        Type: $ => choice($.Type_const, $.Type_func, $.Type_name, $.Type_tuple),
        TypeArray: $ => seq('(', optional(seq($.Type, repeat(seq(',', $.Type)))), ')'),
        Type_const: $ => prec(1, seq(field('0', $.Name), field('1', $.TypeArray))),
        Type_func: $ => prec.right(1, seq(field('0', $.Type), '->', field('1', $.Type))),
        Type_name: $ => field('0', $.Name),
        Type_tuple: $ => field('0', $.TypeArray),
        Typedef: $ => seq('typedef', field('0', $.Name), '=', field('1', $.Type))
    }
})
